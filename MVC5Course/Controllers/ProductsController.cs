using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using MVC5Course.Models;
using PagedList;

namespace MVC5Course.Controllers
{
    
    public class ProductsController : BaseController
    {
        private int DefaultPageSize = 20;
        private FabricsEntities db = new FabricsEntities();
        
        // GET: Products
        public ActionResult Index(string SortBy, string KeyWord = "", int PageNo = 1, bool ShowAll = false)
        {

            var data = ProductRepo.All(ShowAll).AsQueryable();
            if (!string.IsNullOrEmpty(KeyWord))
                data = data.Where(o => o.ProductName.Contains(KeyWord));

            if (SortBy == "+Price")
                data = data.OrderBy(o => o.Price);
            else
                data = data.OrderByDescending(o => o.Price);

            ViewBag.KeyWord = KeyWord;
            ViewBag.SortBy = SortBy;
            ViewBag.PageNo = PageNo;
            ViewBag.ShowAll = ShowAll;
            return View(data.ToPagedList(PageNo, DefaultPageSize));
        }

        // GET: Products/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Product product = ProductRepo.Find(id.Value);
            if (product == null)
            {
                return HttpNotFound();
            }
            return View(product);
        }

        // GET: Products/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Products/Create
        // 若要免於過量張貼攻擊，請啟用想要繫結的特定屬性，如需
        // 詳細資訊，請參閱 http://go.microsoft.com/fwlink/?LinkId=317598。
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "ProductId,ProductName,Price,Active,Stock")] Product product)
        {
            if (ModelState.IsValid)
            {
                ProductRepo.Add(product);
                ProductRepo.UnitOfWork.Commit();                
                return RedirectToAction("Index");
            }

            return View(product);
        }

        // GET: Products/Edit/5
        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Product product = ProductRepo.Find(id.Value);
            if (product == null)
            {
                return HttpNotFound();
            }
            return View(product);
        }

        // POST: Products/Edit/5
        // 若要免於過量張貼攻擊，請啟用想要繫結的特定屬性，如需
        // 詳細資訊，請參閱 http://go.microsoft.com/fwlink/?LinkId=317598。
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "ProductId,ProductName,Price,Active,Stock")] Product product)
        {
            if (ModelState.IsValid)
            {
                var db = ProductRepo.UnitOfWork.Context;
                db.Entry(product).State = EntityState.Modified;
                ProductRepo.UnitOfWork.Commit();
                return RedirectToAction("Index");
            }
            return View(product);
        }

        // GET: Products/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Product product = ProductRepo.Find(id.Value);
            if (product == null)
            {
                return HttpNotFound();
            }
            return View(product);
        }

        // POST: Products/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Product product = ProductRepo.Find(id);
            ProductRepo.Delete(product);
            ProductRepo.UnitOfWork.Commit();
            return RedirectToAction("Index");
        }

